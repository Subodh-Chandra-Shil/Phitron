from math import *
from random import *
from time import sleep

num1 = ceil(4.2214)
print(num1)

num2 = floor(3.14)
print(num2)

# random() gives a random floating value
print(random())

# randint() gives a random integer value
print(randint(1, 100))

# randomly pick any from multiple choices
print(choice([55, 20, 33, 11, 66, 77]))

# sleep() is a method that halts all execution for input time
sleep(1)

